int		ft_intlen(int n, int base)
{
	int		cnt;

	cnt = 1;
	if (n == 0)
		return (1);
	else if (n < 0 && base == 10)
	{
		cnt++;
		n *= -1;
	}
	else if (n < 0)
		n *= -1;
	while (n > 0)
	{
		n /= base;
		cnt++;
	}
	return (cnt);
}
