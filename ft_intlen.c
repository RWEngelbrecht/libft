int		ft_intlen(int n)
{
	int		cnt;

	cnt = 0;
	if (n == 0)
		return (cnt);
	else if (n < 0)
	{
		cnt++;
		while (n /= 10)
			cnt++;
	}
	else
	{
		while (n /= 10)
			cnt++;
	}
	return (cnt);
}
